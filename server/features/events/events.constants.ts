/**
 * Constants for event conflict detection
 */

/**
 * Default event duration in milliseconds (2 hours)
 * Used when an event doesn't have an explicit end time
 */
export const DEFAULT_EVENT_DURATION_MS = 2 * 60 * 60 * 1000;

/**
 * Default event start time when only date is provided
 */
export const DEFAULT_EVENT_TIME = "12:00";
